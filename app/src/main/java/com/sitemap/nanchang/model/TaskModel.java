package com.sitemap.nanchang.model;

import java.io.Serializable;

/**
 * @desc  任务实体类
 * Created by chenmeng on 2016/12/1.
 */

public class TaskModel implements Serializable{
    private String uuid;
    private String taskName;//任务名称
    private String taskNumber;//任务编号
    private String address;//任务地点
    private String company;//目标单位
    private String uploadText;//任务 文字
    private String time;//创建时间

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public String getTaskName() {
        return taskName;
    }

    public void setTaskName(String taskName) {
        this.taskName = taskName;
    }

    public String getTaskNumber() {
        return taskNumber;
    }

    public void setTaskNumber(String taskNumber) {
        this.taskNumber = taskNumber;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getUploadText() {
        return uploadText;
    }

    public void setUploadText(String uploadText) {
        this.uploadText = uploadText;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
